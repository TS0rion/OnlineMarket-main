class Product {
    constructor(Id, Name, Price, Quantity, Image, Description, Origin, MfgDate, ExpDate, CategoryId, BrandId) {
        this.Id = Id;
        this.Name = Name;
        this.Price = Price;
        this.Quantity = Quantity;
        this.Image = Image;
        this.Description = Description;
        this.Origin = Origin;
        this.MfgDate = MfgDate;
        this.ExpDate = ExpDate;
        this.CategoryId = CategoryId;
        this.BrandId = BrandId;
    }
}
export default Product;