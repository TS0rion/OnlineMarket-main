class Store {
    constructor(Id, Address, Name, Description, Status, Coordinates, BrandId, AreaId) {
        this.Id = Id;
        this.Address = Address;
        this.Name = Name;
        this.Description = Description;
        this.Status = Status;
        this.Coordinates = Coordinates;
        this.BrandId = BrandId;
        this.AreaId = AreaId;
    }
}
export default Store;