class Customer{
    constructor(Id,Name,Email,Phone,Birthday,Gender,Status,Image,BuildingId){
        this.Id = Id;
        this.Name = Name;
        this.Email = Email;
        this.Phone = Phone;
        this.Birthday = Birthday;
        this.Gender = Gender;
        this.Status = Status;
        this.Image = Image;
        this.BuildingId = BuildingId;
    }
}
export default Customer;